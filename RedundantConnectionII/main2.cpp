#include <iostream>
#include <vector>

using namespace std;

// hard
// 并查集，有向连接

class Solution {
public:
    vector<int> findRedundantDirectedConnection(vector<vector<int>> &edges) {
        int n = edges.size();
        vector<int> parent(n + 1, 0);
        vector<int> candA;
        vector<int> candB;
        // step 1, check whether there is a node with two parents

        for (auto &edge:edges) {
            if (parent[edge[1]] == 0)
                parent[edge[1]] = edge[0];
            else {
                candA = {parent[edge[1]], edge[1]};
                candB = edge;
                edge[1] = 0;
            }
        }

        // step 2, union find
        for (int i = 1; i <= n; i++)
            parent[i] = i;

        for (auto &edge:edges) {
            if (edge[1] == 0)
                continue;
            int u = edge[0];
            int v = edge[1];
            int pu = root(parent, u);
            // Now every node only has 1 parent, so root of v is implicitly v
            if (pu == v) {
                if (candA.empty())
                    return edge;

                return candA;
            }
            parent[v] = pu;
        }
        return candB;
    }

private:
    int root(vector<int> &parent, int k) {
        if (parent[k] != k)
            parent[k] = root(parent, parent[k]);
        return parent[k];
    }
};

